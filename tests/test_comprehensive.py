class TestCombatFailureModes:
    """Verify combat raises clear errors on bad input."""

    def test_1d_input_rejected(self):
        with pytest.raises(ValueError, match="2-D"):
            combat(np.array([1.0, 2.0, 3.0]), np.array([0, 0, 1]))

    def test_3d_input_rejected(self):
        with pytest.raises(ValueError, match="2-D"):
            combat(np.zeros((2, 3, 4)), np.array([0, 0, 1]))

    def test_nan_raises(self):
        data = np.array([[1.0, 2.0, 3.0, 4.0], [np.nan, 6.0, 7.0, 8.0]])
        with pytest.raises(ValueError, match="NaN"):
            combat(data, np.array([0, 0, 1, 1]))

    def test_single_feature_rejected(self):
        with pytest.raises(ValueError, match=">= 2"):
            combat(np.array([[1.0, 2.0, 3.0, 4.0]]), np.array([0, 0, 1, 1]))

    def test_batch_length_mismatch(self):
        data = np.random.default_rng(1).normal(size=(5, 6))
        with pytest.raises(ValueError, match="batch length"):
            combat(data, np.array([0, 0, 1]))

    def test_invalid_ref_batch(self):
        data = np.random.default_rng(1).normal(size=(5, 6))
        batch = np.array([0, 0, 0, 1, 1, 1])
        with pytest.raises(ValueError, match="ref_batch"):
            combat(data, batch, ref_batch=99)

    def test_single_batch_returns_copy(self):
        """Single batch → data returned unchanged."""
        data = np.random.default_rng(2).normal(10, 2, size=(10, 4))
        batch = np.array([0, 0, 0, 0])
        result = combat(data, batch)
        np.testing.assert_array_equal(result, data)
        # Must be a copy, not the same object
        assert result is not data

    def test_empty_features_many_samples(self):
        """Two features, many samples — should not crash."""
        rng = np.random.default_rng(3)
        data = rng.normal(10, 1, size=(2, 20))
        data[:, 10:] += 3.0
        batch = np.array([0] * 10 + [1] * 10)
        result = combat(data, batch, par_prior=True, mean_only=True)
        assert result.shape == data.shape
        assert not np.isnan(result).any()


class TestCombatEdgeCaseBehavior:
    """Behavioral edge cases — not errors, but tricky inputs."""

    def test_noncontiguous_labels(self):
        """Labels like [5, 5, 10, 10] work via remapping."""
        rng = np.random.default_rng(10)
        data = rng.normal(10, 2, size=(20, 8))
        data[:, 4:] += 3.0
        batch = np.array([5, 5, 5, 5, 10, 10, 10, 10])
        result = combat(data, batch)
        assert result.shape == data.shape
        assert not np.isnan(result).any()

    def test_string_int_labels(self):
        """Batch labels as strings representing ints should work after conversion."""
        rng = np.random.default_rng(11)
        data = rng.normal(10, 2, size=(10, 6))
        data[:, 3:] += 2.0
        batch = np.array([1, 1, 1, 2, 2, 2])
        result = combat(data, batch)
        assert result.shape == data.shape

    def test_ref_batch_preserves_reference(self):
        """Reference batch data must be exactly unchanged."""
        rng = np.random.default_rng(12)
        data = rng.normal(10, 2, size=(20, 9))
        data[:, 3:6] += 3.0
        data[:, 6:] -= 2.0
        batch = np.array([0, 0, 0, 1, 1, 1, 2, 2, 2])
        for ref_b in [0, 1, 2]:
            result = combat(data, batch, ref_batch=ref_b)
            ref_cols = batch == ref_b
            np.testing.assert_array_equal(
                result[:, ref_cols], data[:, ref_cols],
                err_msg=f"ref_batch={ref_b}: reference data changed",
            )

    def test_ref_batch_with_noncontiguous_labels(self):
        """ref_batch refers to original labels, not remapped ones."""
        rng = np.random.default_rng(13)
        data = rng.normal(10, 2, size=(15, 8))
        data[:, 4:] += 4.0
        batch = np.array([10, 10, 10, 10, 20, 20, 20, 20])
        result = combat(data, batch, ref_batch=10)
        ref_cols = batch == 10
        np.testing.assert_array_equal(result[:, ref_cols], data[:, ref_cols])

    def test_identical_batches_no_change(self):
        """If batches have no real effect, output should ≈ input."""
        rng = np.random.default_rng(14)
        data = rng.normal(10, 1, size=(30, 8))
        batch = np.array([0, 0, 0, 0, 1, 1, 1, 1])
        result = combat(data, batch, par_prior=True, mean_only=True)
        # Should be very close to input since no real batch effect
        np.testing.assert_allclose(result, data, atol=1.0,
                                   err_msg="Large change with no real batch effect")

    def test_all_negative_data(self):
        """Negative data should work fine (e.g., log2FC)."""
        rng = np.random.default_rng(15)
        data = rng.normal(-5, 2, size=(20, 8))
        data[:, 4:] -= 3.0
        batch = np.array([0, 0, 0, 0, 1, 1, 1, 1])
        result = combat(data, batch)
        assert result.shape == data.shape
        assert not np.isnan(result).any()

    def test_very_large_values(self):
        """Data with very large values should not overflow."""
        rng = np.random.default_rng(16)
        data = rng.normal(1e6, 1e4, size=(10, 6))
        data[:, 3:] += 1e5
        batch = np.array([0, 0, 0, 1, 1, 1])
        result = combat(data, batch, par_prior=True, mean_only=True)
        assert not np.isnan(result).any()
        assert np.isfinite(result).all()

    def test_very_small_values(self):
        """Near-zero data should not cause division issues."""
        rng = np.random.default_rng(17)
        data = rng.normal(1e-10, 1e-12, size=(10, 6))
        data[:, 3:] += 1e-10
        batch = np.array([0, 0, 0, 1, 1, 1])
        result = combat(data, batch, par_prior=True, mean_only=True)
        assert not np.isnan(result).any()
        assert np.isfinite(result).all()

    def test_many_batches(self):
        """Works with many batches (10)."""
        rng = np.random.default_rng(18)
        n_batches = 10
        n_per = 3
        data = rng.normal(10, 2, size=(20, n_batches * n_per))
        batch = np.repeat(range(n_batches), n_per)
        for b in range(n_batches):
            data[:, batch == b] += rng.normal(0, 2)
        result = combat(data, batch, par_prior=True, mean_only=True)
        assert result.shape == data.shape
        assert not np.isnan(result).any()

    def test_wrapper_mode_dispatch(self):
        """adjust_combat correctly dispatches all 4 modes."""
        rng = np.random.default_rng(19)
        df = pd.DataFrame(
            rng.normal(10, 2, size=(15, 8)),
            index=[f"p_{i}" for i in range(15)],
            columns=[f"s_{j}" for j in range(8)],
        )
        df.iloc[:, 4:] += 3.0
        batch = np.array([0, 0, 0, 0, 1, 1, 1, 1])
        for mode in [1, 2, 3, 4]:
            result = adjust_combat(df, batch, mode=mode)
            assert isinstance(result, pd.DataFrame)
            assert list(result.index) == list(df.index)
            assert list(result.columns) == list(df.columns)

    def test_wrapper_single_batch_passthrough(self):
        """adjust_combat with single batch returns copy."""
        df = pd.DataFrame(
            np.random.default_rng(20).normal(size=(5, 4)),
            index=[f"p_{i}" for i in range(5)],
            columns=[f"s_{j}" for j in range(4)],
        )
        batch = np.array([1, 1, 1, 1])
        result = adjust_combat(df, batch, mode=1)
        pd.testing.assert_frame_equal(result, df)



_MODE_PARAMS = {
    1: {"par_prior": True, "mean_only": False},
    2: {"par_prior": True, "mean_only": True},
    3: {"par_prior": False, "mean_only": False},
    4: {"par_prior": False, "mean_only": True},
}


def _combat_test_cases():
    """Yield (dataset_name, mode) tuples for parametrize."""
    for name, spec in _DATASETS.items():
        for mode in spec["modes"]:
            fixture = spec["fixture"].format(mode)
            if _fixture_exists(fixture):
                yield pytest.param(name, mode, id=f"{name}-mode{mode}")


@pytest.mark.skipif(
    not _fixture_exists("large_combat_mode1.tsv"),
    reason="Edge-case R fixtures not generated",
)
class TestCombatRConcordanceExtended:
    """ComBat output must match R sva::ComBat across diverse scenarios."""

    @pytest.mark.parametrize("dataset,mode", list(_combat_test_cases()))
    def test_vs_r_sva(self, dataset, mode):
        spec = _DATASETS[dataset]
        data = _load_tsv(spec["input"])
        batch = _load_batch(spec["batch"])
        expected = _load_tsv(spec["fixture"].format(mode))
        params = _MODE_PARAMS[mode]

        result = combat(data, batch, **params)

        # Mode 1 (parametric iterative) has slightly higher tolerance
        rtol = 1e-4 if mode == 1 else 1e-6
        atol = 1e-5 if mode == 1 else 1e-8
        # Nonparametric modes on large data can have slightly more divergence
        if mode == 3 and dataset == "large":
            rtol = 5e-4
            atol = 1e-4

        # R produces NaN for near-constant features (pooled var ≈ 0);
        # our implementation clamps var_pooled > 0 and returns numbers.
        # Compare only where R produced non-NaN, then verify R-NaN rows
        # are from near-constant features (low within-batch variance).
        r_nan_mask = np.isnan(expected)
        if r_nan_mask.any():
            # Rows where R says NaN — verify they have near-zero residual var
            nan_rows = np.where(r_nan_mask.any(axis=1))[0]
            unique_b = np.unique(batch)
            for r in nan_rows:
                # Check within-batch variance (what R actually uses for pooled var)
                within_vars = [data[r, batch == b].var(ddof=1)
                               for b in unique_b if (batch == b).sum() > 1]
                max_within_var = max(within_vars) if within_vars else 0
                assert max_within_var < 0.1, (
                    f"R output NaN for row {r} but max within-batch "
                    f"var={max_within_var:.2e}"
                )
            # Compare non-NaN portion
            valid = ~r_nan_mask
            if valid.any():
                np.testing.assert_allclose(
                    result[valid], expected[valid], rtol=rtol, atol=atol,
                    err_msg=f"{dataset} mode {mode}: mismatch vs R sva::ComBat",
                )
        else:
            np.testing.assert_allclose(
                result, expected, rtol=rtol, atol=atol,
                err_msg=f"{dataset} mode {mode}: mismatch vs R sva::ComBat",
            )

    @pytest.mark.parametrize("dataset,mode", list(_combat_test_cases()))
    def test_shape_preserved(self, dataset, mode):
        spec = _DATASETS[dataset]
        data = _load_tsv(spec["input"])
        batch = _load_batch(spec["batch"])
        result = combat(data, batch, **_MODE_PARAMS[mode])
        assert result.shape == data.shape

    @pytest.mark.parametrize("dataset,mode", list(_combat_test_cases()))
    def test_no_nan_in_output(self, dataset, mode):
        spec = _DATASETS[dataset]
        data = _load_tsv(spec["input"])
        batch = _load_batch(spec["batch"])
        result = combat(data, batch, **_MODE_PARAMS[mode])
        assert not np.isnan(result).any(), f"NaN in {dataset} mode {mode}"

    @pytest.mark.parametrize("dataset,mode", list(_combat_test_cases()))
    def test_batch_means_converge(self, dataset, mode):
        """After correction, batch means should be more similar."""
        spec = _DATASETS[dataset]
        data = _load_tsv(spec["input"])
        batch = _load_batch(spec["batch"])
        result = combat(data, batch, **_MODE_PARAMS[mode])

        unique_b = np.unique(batch)
        means_before = [data[:, batch == b].mean() for b in unique_b]
        means_after = [result[:, batch == b].mean() for b in unique_b]
        spread_before = max(means_before) - min(means_before)
        spread_after = max(means_after) - min(means_after)
        assert spread_after < spread_before + 1e-10, (
            f"{dataset} mode {mode}: spread not reduced "
            f"{spread_before:.4f} → {spread_after:.4f}"
        )


